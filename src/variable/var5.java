package variable;

public class var5 {
    public static void main(String[] args) {
        // 1. 변수 선언. 초기화 각각 따로
        int a;
        a = 1;
        System.out.println(a);

        int b = 2; // 선언과 초기화를 한번에하기
        System.out.println(b);

        int c = 3, d = 4; // 여러 번수 선언과 초기화를 한번에 할 수 있음
        System.out.println(c);
        System.out.println(d);

    }
}
